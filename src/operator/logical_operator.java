package operator;

public class logical_operator{
	
	public static void main(String[]args) {
		
		
		boolean a=true;
		boolean b=false;
		
		System.out.println(a&&b);  // AND OPEERATOR
		
		System.out.println(a||b);  // OR OPERATOR
		
		System.out.println(!a);
		
		System.out.println(!b);
	}
	
	
	
	
	
}